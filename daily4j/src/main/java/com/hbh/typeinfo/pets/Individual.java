package com.hbh.typeinfo.pets;

public class Individual {
	
	private int id;
	private String name;
	
	public Individual(String name) {
		this.name = name;
	}
	
	public Individual() {
		
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
